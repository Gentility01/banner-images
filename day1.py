# strings
"""Strings are sequences of characters and are used to represent text.
They are enclosed in single (' ') or double (" ") quotes."""

print("11111111123456789")
print("nobles's")
print(type("noble"))

# numbers --> intergers and float
print(20000)
print(type(2000))

print(2.3)
print(type(2.3))

# list
"""
Lists are ordered collections of items, which can be of different data types.
They are defined using square brackets [].
Lists can be modified (mutable), meaning you can add, remove, or change elements.

"""
print(["noble", 30, 21.2, [1,2,3,4,5,6]])
print(type(["noble", 30, 21.2, [1,2,3,4,5,6]]))

